                <h2 class="aboutsectitle">程式設計競賽入門</h2>
                <p>
                    想要參加<strong>程式設計競賽</strong>，卻不知道怎麼準備嗎？<br/>
                </p>
                <h3 style="color: red">那你應該來聽這系列的課程</h3><br/>
                <!--(有人會發現這行嗎？)講師CPE最高是4題、103 NCPC 決賽佳作-->
                <p>
                    地點：資電235<br/>
                    時間：18:30 ~ 21:00
                </p>
                <p>
                    有句話說：<br/>
                </p>
                <blockquote><p>資料結構 + 演算法 = 程式</p></blockquote>
                <p>
                    在競賽中，<strong>資料結構</strong>和<strong>演算法</strong>是必備的知識<br/>因此在入門課程裡會幫各位上點基礎的<strong>資料結構</strong>和<strong>演算法</strong>，<strong>資料結構</strong>比較會著重在使用，而不是實作，而<strong>演算法</strong>會著重概念上。
                </p>
                <ul>
                    <li>資料結構</li>
                    <ul>
                        <li>vector</li>
                        <li>linked list</li>
                        <li>stack</li>
                        <li>queue</li>
                        <li>set</li>
                        <li>heap</li>
                        <li>graph</li>
                        <li>map</li>
                    </ul>
                    <li>演算法</li>
                    <ul>
                        <li>時間複雜度</li>
                        <li>DFS、BFS</li>
                        <li>各個擊破</li>
                        <li>動態規劃</li>
                        <li>貪婪演算法</li>
                        <li>圖論(最短路徑、最小生成樹)</li>
                    </ul>
                </ul><br/>
                <h3 class="aboutsectitle">預備知識</h3>
